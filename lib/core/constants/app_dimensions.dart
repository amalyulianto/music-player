/// Defines layout, spacing, corner radius, and component size constants
/// for the Music Player application.
///
/// Standardizes dimensions to ensure responsive and premium visual alignment.
abstract class AppDimensions {
  /// Extra small spacing value (4.0).
  static const double paddingXs = 4.0;

  /// Small spacing value (8.0).
  static const double paddingSm = 8.0;

  /// Medium spacing value (16.0).
  static const double paddingMd = 16.0;

  /// Large spacing value (24.0).
  static const double paddingLg = 24.0;

  /// Extra large spacing value (32.0).
  static const double paddingXl = 32.0;

  /// Small corner radius (8.0).
  static const double radiusSm = 8.0;

  /// Medium corner radius (12.0).
  static const double radiusMd = 12.0;

  /// Large corner radius (16.0).
  static const double radiusLg = 16.0;

  /// Extra large corner radius (50.0), typically used for circular clipping.
  static const double radiusXl = 50.0;

  /// Small icon size (16.0).
  static const double iconSm = 16.0;

  /// Medium icon size (24.0).
  static const double iconMd = 24.0;

  /// Large icon size (32.0).
  static const double iconLg = 32.0;

  /// Extra large icon size (48.0).
  static const double iconXl = 48.0;

  /// The height of the mini player bar at the bottom of the screens (80.0).
  static const double miniPlayerHeight = 80.0;

  /// The standard size for recently playing card images (60.0).
  static const double recentlyPlayingCardSize = 60.0;

  /// The standard size for the album art cover on the now playing screen (300.0).
  static const double albumArtSize = 300.0;
}
